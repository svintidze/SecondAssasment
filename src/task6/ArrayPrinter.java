package task6;

public class ArrayPrinter {
    void printArray(int[] numbers) {
        for (int i = 0; i < numbers.length; i++) {
            System.out.println(numbers[i]);
        }
    }
    void printArray(String[] texts){
        for (int i = 0; i < texts.length; i++) {
            System.out.println(texts[i]);
        }
    }
}
